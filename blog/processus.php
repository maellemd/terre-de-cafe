    <section id="article-blog" class="section1">
        <div class="blog-content">
            <p class="intro">Le café est bien plus qu'une simple boisson ; c'est une histoire de passion, de savoir-faire et de traditions qui remonte à plusieurs siècles.</p>

            <div class="article-section">
                <h3>1. La culture du caféier</h3>
                <p>Tout commence par la culture du caféier, un arbuste tropical qui pousse principalement dans la "ceinture du café", une zone située entre les tropiques du Cancer et du Capricorne.</p>
                <div class="varietes">
                    <div class="variete-box">
                        <h4>Arabica</h4>
                        <p>Apprécié pour ses arômes complexes et son acidité raffinée.</p>
                    </div>
                    <div class="variete-box">
                        <h4>Robusta</h4>
                        <p>Reconnu pour sa teneur plus élevée en caféine et son goût plus corsé.</p>
                    </div>
                </div>
            </div>

            <div class="article-section">
                <h3>2. La récolte des cerises</h3>
                <p>Lorsque les cerises de café atteignent leur pleine maturité, elles prennent une teinte rouge vif.</p>
                <div class="methode">
                    <div class="methode-box">
                        <h4>Récolte manuelle (picking)</h4>
                        <p>Cueillette soigneuse des cerises les plus mûres, privilégiée pour les grands crus.</p>
                    </div>
                    <div class="methode-box">
                        <h4>Récolte mécanisée (stripping)</h4>
                        <p>Méthode plus rapide, récoltant toutes les cerises d'une branche simultanément.</p>
                    </div>
                </div>
            </div>

            <div class="article-section">
                <h3>3. Le traitement des grains</h3>
                <p>Les cerises sont rapidement traitées pour préserver leur qualité.</p>
                <div class="processus">
                    <div class="processus-box">
                        <h4>Traitement par voie humide</h4>
                        <p>Dépulpage, fermentation, et séchage contrôlé.</p>
                    </div>
                    <div class="processus-box">
                        <h4>Traitement par voie sèche</h4>
                        <p>Séchage des cerises entières au soleil avant décorticage.</p>
                    </div>
                </div>
            </div>

            <div class="article-section">
                <h3>4. Le tri et la torréfaction</h3>
                <p>Les grains sont soigneusement triés puis torréfiés, développant leurs arômes uniques.</p>
                <div class="torrefaction">
                    <div class="torrefaction-box">
                        <h4>Torréfaction légère</h4>
                        <p>Met en valeur les notes fruitées et florales.</p>
                    </div>
                    <div class="torrefaction-box">
                        <h4>Torréfaction moyenne</h4>
                        <p>Équilibre parfait entre acidité et amertume.</p>
                    </div>
                    <div class="torrefaction-box">
                        <h4>Torréfaction foncée</h4>
                        <p>Arômes intenses de caramel et de chocolat.</p>
                    </div>
                </div>
            </div>

            <div class="article-section">
                <h3>5. Le broyage et l'extraction</h3>
                <p>La dernière étape transforme les grains en cette boisson précieuse que nous adorons.</p>
                <div class="extraction-details">
                    <p>Le broyage est adapté à chaque méthode de préparation, de l'espresso à la presse française.</p>
                    <p>L'extraction révèle toute la richesse aromatique du café, créant une expérience unique à chaque tasse.</p>
                </div>
            </div>
        </div>
    </section>
